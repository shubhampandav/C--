#include<iostream>
using namespace std;

int main(){
    int a = 2;
    int b = 3;

    cout<<"a&b = "<<(a&b)<<endl; // bitwise and operator
    cout<<"a|b = "<<(a|b)<<endl;  // or operator 
    cout<<"a^b = "<<(a^b)<<endl;    // xor operator 
   cout<<(2<<1)<<endl;    // left shift operator
   cout<<(2>>1);  // Right shift operator

     

}